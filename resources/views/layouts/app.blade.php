        /**
         * Register our service workers before initializing the app
         * because we want the request that the app's going to make
         * to be first processed by the registered worker.
         */
        if ('serviceWorker' in navigator) {
            window.addEventListener('load', () => {
                navigator.serviceWorker.register('service-worker.js', {
                        scope: '/home'
                    })
                    .then((reg) => {
                        return navigator.serviceWorker.ready;
                    })
                    .catch(err => console.log(`${Date.now()} - ${err}`));

                navigator.serviceWorker.ready.then(function(serviceWorkerRegistration) {
                        // Let's see if you have a subscription already
                        return serviceWorkerRegistration.pushManager.getSubscription();
                    })
                    .then(function(subscription) {
                        if (!subscription) {
                            // You do not have subscription
                        }
                        // You have subscription.
